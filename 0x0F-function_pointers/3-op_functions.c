#include "3-calc.h"

/**
* op_add - add b to a
* @a: parameter
* @b: parameter
*
* Return: result (Success)
*/
int op_add(int a, int b)
{
return (a + b);
}


/**
* op_sub - subtract b from a
* @a: parameter
* @b: parameter
*
* Return: result (Success)
*/
int op_sub(int a, int b)
{
return (a - b);
}


/**
* op_mul - multiply a and b
* @a: parameter
* @b: parameter
*
* Return: result (Success)
*/
int op_mul(int a, int b)
{
return ((a) * (b));
}


/**
* op_div - add a by b
* @a: parameter
* @b: parameter
*
* Return: result (Success)
*/
int op_div(int a, int b)
{
return (a / b);
}


/**
* op_mod - a module b
* @a: parameter
* @b: parameter
*
* Return: result (Success)
*/
int op_mod(int a, int b)
{
return (a % b);
}
